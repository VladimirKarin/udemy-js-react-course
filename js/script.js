'use strict';

// INTERPOLATION
// interpolation is the process of inserting strings or values into an existing string for various purposes.

const category = 'toys';

console.log(`http://someawesomeurl.com/${category}`);

// CONCATENATION
// The same + operator you use for adding two numbers can be used to concatenate two strings.

console.log('http://someawesomeurl.com/' + category);

const user = 'Vladimir';
alert(`Hello, ${user}!`);
